    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        
        String h = "kosong"; 
        if(lk.isSelected())
        {
            jk = "L";
        }
        else
        {
            jk = "P";
        }
        
       
        if((String) hk.getSelectedItem() == "Kepala Keluarga")
            h = "KK";
        else if((String) hk.getSelectedItem() == "Ibu")
            h = "I";
        else if((String) hk.getSelectedItem() == "Anak")
            h = "A";
        
        
        if(nama.getText().equals("") || h.equals("kosong") || jk.equals("kosong") || tempat.getText().equals("") || tanggal.getDate() == null || pend.getText().equals("") || pekerjaan.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Data tidak lengkap");
        }
        
        else
        {
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        tgl = String.valueOf(format.format(tanggal.getDate()));
        
       
        
            try {
            Connection c = KoneksiDB.getkoneksi();
            String sql = "Insert into individu values (0,0,?,?,?,?,?,?,?)";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            p.setString(1, nama.getText());
            p.setString(2, h);
            p.setString(3, jk);
            p.setString(4, tempat.getText());
            p.setString(5, tgl);
            p.setString(6, pend.getText());
            p.setString(7, pekerjaan.getText());

            
            p.executeUpdate();
            p.close();

            nama.setText("");
            tempat.setText("");
            pend.setText("");
            pekerjaan.setText("");
            JOptionPane.showMessageDialog(null, "Data telah dimasukan");
        }catch(SQLException e){
            System.out.println(e);
        }finally{
            loadData();
        }
        }
        
        
    }//GEN-LAST:event_simpanActionPerformed

