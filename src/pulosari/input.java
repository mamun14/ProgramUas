    private void finishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishActionPerformed
        // TODO add your handling code here:
        String n = null;
        String ik = null;
        
        if(kampung.getText().equals("") || rt.getText().equals("") || rw.getText().equals("") || dusun.getText().equals("") || 
                kec.getText().equals("") || kab.getText().equals("") || prov.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Data alamat tidak lengkap");
        }
        
        else
        {
        
        try {            
            Connection c = KoneksiDB.getkoneksi();
            Statement s = c.createStatement();
            String sql = "Select * from individu where id_kel = 0 and hub = 'KK'";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()) {
                            
                n=r.getString("nama");
                
            }
            r.close();
            s.close();
        }catch(SQLException e) {
            System.out.println("Terjadi kesalahan"); 
            System.out.println(e);
        }
        
        try {
            Connection c = KoneksiDB.getkoneksi();
            String sql = "Insert into keluarga values (0,?,?,?,?,?,?,?,?)";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            p.setString(1, n);
            p.setString(2, kampung.getText());
            p.setString(3, rt.getText());
            p.setString(4, rw.getText());
            p.setString(5, dusun.getText());
            p.setString(6, kec.getText());
            p.setString(7, kab.getText());
            p.setString(8, prov.getText());
            
            p.executeUpdate();
            p.close();

            kampung.setText("");
            rt.setText("");
            rw.setText("");
            dusun.setText("");
            kec.setText("");
            kab.setText("");
            prov.setText("");
            
            kampung.setEnabled(false);
            rt.setEnabled(false);
            rw.setEnabled(false);
            dusun.setEnabled(false);
            kec.setEnabled(false);
            kab.setEnabled(false);
            prov.setEnabled(false);

            simpan.setEnabled(true);
            selesai.setEnabled(true);
            finish.setEnabled(false);

            nama.setEnabled(true);
            hk.setEnabled(true);
            tempat.setEnabled(true);
            pend.setEnabled(true);
            pekerjaan.setEnabled(true);
            
            JOptionPane.showMessageDialog(null, "Input data berhasil dilakukan");
        }catch(SQLException e){
            System.out.println(e);
        }
        
        try {            
            Connection c = KoneksiDB.getkoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM keluarga ORDER BY id_kel DESC LIMIT 1 ";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()) {
                            
                ik = r.getString("id_kel");
                
            }
            r.close();
            s.close();
        }catch(SQLException e) {
            System.out.println("Terjadi kesalahan"); 
            System.out.println(e);
        }

        try {
            Connection c = KoneksiDB.getkoneksi();
            String sql = "UPDATE individu Set id_kel = ? WHERE id_kel = 0";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            
            p.setString(1, ik);
          
            p.executeUpdate();
            p.close();
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
        }catch(SQLException e){
            System.out.println("Terjadi kesalahan");
        }
        }
    }//GEN-LAST:event_finishActionPerformed

